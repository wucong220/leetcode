	static public int step(int[][] obstacleGrid, int x, int y) {
		int h = obstacleGrid.length;
		int w = obstacleGrid[0].length;
		// x,y是机器人所在坐标

		if (x == w || y == h || obstacleGrid[y][x] == 1) {
			return 0;
		}

		if (x == w - 1 && y == h - 1) {
			return 1;
		}

		return step(obstacleGrid, x + 1, y) + step(obstacleGrid, x, y + 1);

	}

	static public int uniquePathsWithObstacles(int[][] obstacleGrid) {
		// x,y是机器人所在坐标
		int x = 0;
		int y = 0;

		return step(obstacleGrid, x, y);
	}

	static public int uniquePathsWithObstacles1(int[][] obstacleGrid) {
		int[] destination = new int[] { obstacleGrid.length - 1, obstacleGrid[0].length - 1 };
		Queue<int[]> q = new ArrayDeque<>();
		q.offer(new int[] { 0, 0 });
		int ret = 0;
		while (!q.isEmpty()) {
			int[] position = q.poll();

			if (obstacleGrid[position[0]][position[1]] == 1)
				continue;

			if (position[0] == destination[0] && position[1] == destination[1]) {
				ret++;
				continue;
			}

			if (position[0] < destination[0]) {
				q.offer(new int[] { position[0] + 1, position[1] });
			}

			if (position[1] < destination[1]) {
				q.offer(new int[] { position[0], position[1] + 1 });
			}
		}
		return ret;
	}

	static public int uniquePathsWithObstacles2(int[][] obstacleGrid) {
		int[] d = new int[obstacleGrid[0].length];

		for (int i = 0; i < obstacleGrid.length; i++) {
			for (int j = 0; j < d.length; j++) {
				if (obstacleGrid[i][j] == 1) {
					d[j] = 0;
				} else if (i == 0 && j == 0) {
					d[i] = 1;
				} else if (j == 0) {
					d[j] = d[j];
				} else {
					d[j] = d[j] + d[j - 1];
				}
			}
		}
		return d[d.length-1];
	}

